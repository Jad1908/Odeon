"""
Newsletter workflow UI.

Single Flask app driving the full issue workflow: fetch (cached) -> select
movies -> organize into sections + comment -> prerender -> validate.
Run from the repo root: python viewer.py
"""
import json
import os

from flask import Flask, render_template, jsonify, request, send_file

from pipeline.runner import pipeline, get_cache_info
from pipeline import issue as issue_store
from pipeline import prerender as prerender_mod
from pipeline import validation as validation_mod
from pipeline import send as send_mod
from pipeline.analysis import get_normalized_score

app = Flask(__name__)

# Category display names and descriptions (browsing lenses over the scrape)
CATEGORIES = {
    "top_new_releases": {
        "name": "Top New Releases",
        "description": "Brand new movies released within the last 7 days"
    },
    "current_landscape": {
        "name": "Current Landscape",
        "description": "Recent movies still playing in theaters (1-8 weeks old)"
    },
    "premieres_events": {
        "name": "Premieres & Events",
        "description": "Special premiere screenings and events"
    },
    "old_classics": {
        "name": "Old Classics",
        "description": "Classic films returning to the big screen"
    },
    "letterboxd_picks": {
        "name": "Letterboxd Picks",
        "description": "Highly rated on Letterboxd (7.0+)"
    },
    "niche_gems": {
        "name": "Niche Gems",
        "description": "High quality films with limited screenings"
    }
}


def load_categorized() -> dict:
    """Newsletter categories generated by the analysis step."""
    try:
        with open('data/newsletter_data.json', 'r', encoding='utf-8') as f:
            return json.load(f)
    except (FileNotFoundError, json.JSONDecodeError):
        return {}


def load_all_movies() -> list:
    """Every scraped movie, scored and sorted, for the 'All movies' tab."""
    try:
        with open('data/week_full.json', 'r', encoding='utf-8') as f:
            movies = json.load(f)
    except (FileNotFoundError, json.JSONDecodeError):
        return []
    for m in movies:
        m['calculated_score'] = get_normalized_score(m)
    movies.sort(key=lambda m: m['calculated_score'], reverse=True)
    return movies


def build_select_movies() -> list:
    """Slim, deduplicated movie list for the selection UI.

    Each movie appears once, tagged with every category it belongs to,
    so the client can filter without showing duplicates.
    """
    categorized = load_categorized()
    membership = {}
    for key in CATEGORIES:
        for m in categorized.get(key, []):
            membership.setdefault(str(m['id']), []).append(key)

    slim = []
    for m in load_all_movies():
        mid = str(m['id'])
        slim.append({
            "id": mid,
            "title": m['title'],
            "original_title": m.get('original_title') or "",
            "director": m.get('director') or "",
            "actors": m.get('actors') or "",
            "year": m.get('year'),
            "genre": m.get('genre') or "",
            "duration": m.get('duration_minutes'),
            "copies": m.get('copies_count') or 0,
            "showtimes": len(m.get('showtimes') or []),
            "poster_url": m.get('poster_url'),
            "score": m['calculated_score'],
            "ratings": [{"s": r['source'], "v": r['score'], "m": r['max_score']}
                        for r in m.get('ratings', [])],
            "cats": membership.get(mid, []),
            "release_date": m.get('release_date'),
            "premiere": bool(m.get('is_premiere')),
            "new_release": bool(m.get('is_new_release')),
        })
    return slim


@app.route('/')
def index():
    return render_template(
        'viewer.html',
        select_movies=build_select_movies(),
        categories=CATEGORIES,
        sections=issue_store.load_sections(),
        issue=issue_store.load_issue(),
        cache=get_cache_info()
    )


@app.route('/api/movies/<movie_id>')
def movie_details(movie_id):
    """Full metrics for one movie: ratings with links, screening breakdown."""
    from collections import Counter
    lookup = prerender_mod.load_movie_lookup()
    m = lookup.get(str(movie_id))
    if m is None:
        return jsonify({"error": "Unknown movie"}), 404

    showtimes = m.get('showtimes') or []
    cinema_counts = Counter(st.get('cinema_name') for st in showtimes if st.get('cinema_name'))
    version_counts = Counter(st.get('version') or '?' for st in showtimes)

    return jsonify({
        "id": str(m['id']),
        "title": m['title'],
        "original_title": m.get('original_title') or "",
        "director": m.get('director') or "",
        "actors": m.get('actors') or "",
        "year": m.get('year'),
        "genre": m.get('genre') or "",
        "language": m.get('language') or "",
        "duration_minutes": m.get('duration_minutes'),
        "release_date": m.get('release_date'),
        "availability_status": m.get('availability_status'),
        "is_new_release": bool(m.get('is_new_release')),
        "is_premiere": bool(m.get('is_premiere')),
        "is_retrospective": bool(m.get('is_retrospective')),
        "poster_url": m.get('poster_url'),
        "calculated_score": m['calculated_score'],
        "copies_count": m.get('copies_count') or 0,
        "ratings": m.get('ratings') or [],
        "stats": {
            "total_showtimes": len(showtimes),
            "cinema_count": len(cinema_counts),
            "cinemas": [{"name": name, "count": count}
                        for name, count in cinema_counts.most_common()],
            "versions": dict(version_counts.most_common())
        }
    })


# ---- Pipeline (fetch with cache) ----

@app.route('/api/pipeline/status')
def pipeline_status():
    state = pipeline.get_state()
    state["cache"] = get_cache_info()
    return jsonify(state)


@app.route('/api/pipeline/fetch', methods=['POST'])
def start_fetch():
    force = bool((request.get_json(silent=True) or {}).get('force'))
    return jsonify(pipeline.run_fetch(force=force))


@app.route('/api/pipeline/reset', methods=['POST'])
def reset_pipeline():
    pipeline.reset()
    return jsonify({"status": "reset"})


# ---- Issue draft ----

@app.route('/api/issue', methods=['PUT'])
def save_issue():
    payload = request.get_json(force=True)
    issue = issue_store.save_issue(
        payload.get('movies', {}),
        content=payload.get('content')
    )
    return jsonify(issue)


# ---- Sections ----

@app.route('/api/sections', methods=['POST'])
def add_section():
    payload = request.get_json(force=True)
    try:
        section = issue_store.add_section(
            payload.get('title', ''), payload.get('description', ''))
    except ValueError as e:
        return jsonify({"error": str(e)}), 400
    return jsonify(section)


@app.route('/api/sections/order', methods=['PUT'])
def reorder_sections():
    payload = request.get_json(force=True)
    try:
        sections = issue_store.reorder_sections(payload.get('keys', []))
    except ValueError as e:
        return jsonify({"error": str(e)}), 400
    return jsonify(sections)


@app.route('/api/sections/<key>', methods=['PATCH'])
def update_section(key):
    payload = request.get_json(force=True)
    try:
        section = issue_store.update_section(
            key, payload.get('title'), payload.get('description'))
    except ValueError as e:
        return jsonify({"error": str(e)}), 400
    return jsonify(section)


@app.route('/api/sections/<key>', methods=['DELETE'])
def delete_section(key):
    try:
        issue_store.delete_section(key)
    except ValueError as e:
        return jsonify({"error": str(e)}), 400
    return jsonify({"deleted": key})


# ---- Prerender & validation ----

@app.route('/api/prerender', methods=['POST'])
def run_prerender():
    return jsonify(prerender_mod.run_prerender())


@app.route('/preview')
def preview():
    path = os.path.abspath(prerender_mod.PREVIEW_FILE)
    if not os.path.exists(path):
        return "No preview rendered yet.", 404
    response = send_file(path)
    response.headers['Cache-Control'] = 'no-store'
    return response


@app.route('/api/validate', methods=['POST'])
def validate():
    # Always validate a fresh render of the current draft
    render_result = prerender_mod.run_prerender()
    if not render_result["ok"]:
        return jsonify({"passed": False, "status": "draft", "checks": [],
                        "error": f"Prerender failed: {render_result['log']}"})
    result = validation_mod.run_validation()
    return jsonify(result)


# ---- Send (Resend) ----

@app.route('/api/send/status')
def send_status():
    """Whether Resend is configured, and the default subject for the issue."""
    issue = issue_store.load_issue()
    return jsonify({
        "configured": not send_mod.config_problems(send_mod.get_config()),
        "problems": send_mod.config_problems(send_mod.get_config()),
        "default_subject": send_mod.default_subject(issue),
        "last_send": issue.get("last_send")
    })


@app.route('/api/send', methods=['POST'])
def send_newsletter():
    payload = request.get_json(silent=True) or {}
    result = send_mod.send_newsletter(subject=payload.get('subject'))
    return jsonify(result), (200 if result["ok"] else 400)


if __name__ == '__main__':
    print("Starting Newsletter Workflow UI...")
    print("Open http://localhost:5000 in your browser")
    app.run(debug=True, port=5000)
